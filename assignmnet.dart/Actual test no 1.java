// This program demonstrates the basic syntax of Dart

// Variable declarations
int a = 10; // Integer variable
double b = 3.14; // Floating-point variable
String c = "Hello, world!"; // String variable

// Arithmetic operations
print("a + b = ", a + b); // Addition
print("a - b = ", a - b); // Subtraction
print("a * b = ", a * b); // Multiplication
print("a / b = ", a / b); // Division

// Data types
print("The type of a is ", typeOf(a));
print("The type of b is ", typeOf(b));
print("The type of c is ", typeOf(c));
